/// <summary>
/// Enum of tile layer states
/// </summary>
public enum TileLayerState
{
    /// <summary>
    /// The tile layer was initialized
    /// </summary>
    Initial,
    /// <summary>
    /// The tile layer's data has been loaded
    /// </summary>
    Loaded,
    /// <summary>
    /// The tile layer has been rendered
    /// </summary>
    Rendered
}