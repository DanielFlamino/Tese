/// <summary>
/// Enum of tile states
/// </summary>
public enum TileState
{
    /// <summary>
    /// The tile was initialized
    /// </summary>
    Initial,
    /// <summary>
    /// The tile's data has been loaded
    /// </summary>
    Loaded,
    /// <summary>
    /// The tile has been rendered
    /// </summary>
    Rendered
}